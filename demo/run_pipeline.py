def step_parser(cobol_path: str, output_json: str) -> Dict[str, Any]:
    """
    Agent 1 -- Parser.

    Parse the raw COBOL source into a structured JSON map.
    """
    print("\n" + "=" * 72)
    print("STEP 1/7 -- Parser")
    print("=" * 72)

    parser_path = _REPO_ROOT / "agents" / "parser.py"
    if not parser_path.exists():
        print("[WARN] parser.py not found -- using stub.")
        parse_data = {
            "program_id": "UNKNOWN",
            "data_fields": {},
            "paragraphs": {},
            "conditions": {},
        }
    else:
        parser = _load_module("agents.parser", str(parser_path))
        # The parser module is expected to expose a ``main()`` or
        # ``parse_cobol(source_code)`` style API.  We normalise here.
        if hasattr(parser, "parse_cobol"):
            with open(cobol_path, "r", encoding="utf-8") as fh:
                source = fh.read()
            parse_data = parser.parse_cobol(source)
        elif hasattr(parser, "main"):
            # Fallback: call main with --input and capture output file.
            import tempfile
            tmp_out = tempfile.mktemp(suffix=".json")
            old_argv = sys.argv
            try:
                sys.argv = ["parser.py", "--input", cobol_path, "--output", tmp_out]
                parser.main()
            finally:
                sys.argv = old_argv
            with open(tmp_out, "r", encoding="utf-8") as fh:
                parse_data = json.load(fh)
            os.remove(tmp_out)
        else:
            raise RuntimeError("Parser module has no parse_cobol() or main()")

    _ensure_dir(output_json)
    with open(output_json, "w", encoding="utf-8") as fh:
        json.dump(parse_data, fh, indent=2)

    print(f"[OK]  Parser output written to {output_json}")
    return parse_data


def step_logic(
    parse_data: Dict[str, Any],
    output_md: str,
    output_json: str,
    no_ai: bool = False,
    tracker: Optional[Any] = None,
) -> Dict[str, Any]:
    """
    Agent 2 -- Logic Extractor.

    Extract business rules from the parsed structure.  When *tracker* is
    supplied and AI mode is enabled, token usage is recorded automatically.
    """
    print("\n" + "=" * 72)
    print("STEP 2/7 -- Logic Extractor")
    print("=" * 72)

    extractor_path = _REPO_ROOT / "agents" / "logic_extractor.py"
    if not extractor_path.exists():
        print("[WARN] logic_extractor.py not found -- skipping.")
        return {}

    extractor = _load_module("agents.logic_extractor", str(extractor_path))

    # Run the structured extractors (no API calls).
    extracted: Dict[str, Any] = {
        "inputs": extractor.extract_inputs(parse_data),
        "outputs": extractor.extract_outputs(parse_data),
        "constants": extractor.extract_constants(parse_data),
        "validation_rules": extractor.extract_validation_rules(parse_data),
        "calculation_steps": extractor.extract_calculation_steps(parse_data),
        "conditions": extractor.extract_conditions(parse_data),
    }

    _ensure_dir(output_json)
    with open(output_json, "w", encoding="utf-8") as fh:
        json.dump(extracted, fh, indent=2)
    print(f"[OK]  Logic JSON written to {output_json}")

    # AI interpretation (optional, costs money).
    if no_ai:
        ai_text = "*AI interpretation skipped (--no-ai flag set)*"
        print("[LOGIC] Skipping AI interpretation.")
    else:
        # Pass the tracker so the extractor can record API costs.
        ai_text = extractor.interpret_with_ai(parse_data, extracted, tracker=tracker)

    # Build Markdown report.
    report = extractor.build_report(parse_data, extracted, ai_text)
    _ensure_dir(output_md)
    with open(output_md, "w", encoding="utf-8") as fh:
        fh.write(report)
    print(f"[OK]  Business rules written to {output_md}")

    return extracted


def step_codegen(
    parse_data: Dict[str, Any],
    extracted: Dict[str, Any],
    output_py: str,
) -> str:
    """
    Agent 4 -- Code Generator.

    Generate Python source from the parse tree + business logic.
    """
    print("\n" + "=" * 72)
    print("STEP 4/7 -- Code Generator")
    print("=" * 72)

    # Load the code writer (Agent 4) — tries code_writer.py first,
    # then falls back to code_generator.py for compatibility.
    codegen_path = _REPO_ROOT / "agents" / "code_writer.py"
    if not codegen_path.exists():
        codegen_path = _REPO_ROOT / "agents" / "code_generator.py"

    if codegen_path.exists():
        mod_name = "agents.code_writer" if "code_writer" in str(codegen_path) else "agents.code_generator"
        codegen = _load_module(mod_name, str(codegen_path))
        if hasattr(codegen, "generate"):
            python_code = codegen.generate(parse_data, extracted, output_py)
        else:
            python_code = _stub_codegen(parse_data, extracted)
    else:
        print("[WARN] code_writer.py not found -- using stub.")
        python_code = _stub_codegen(parse_data, extracted)

    _ensure_dir(output_py)
    with open(output_py, "w", encoding="utf-8") as fh:
        fh.write(python_code)
    print(f"[OK]  Python output written to {output_py}")
    return python_code


def step_testgen(parse_data: Dict[str, Any], output_dir: str) -> List[str]:
    """
    Agent 3 -- Test Generator.

    Generate unit tests for the migrated Python code.
    """
    print("\n" + "=" * 72)
    print("STEP 3/7 -- Test Generator")
    print("=" * 72)

    testgen_path = _REPO_ROOT / "agents" / "test_generator.py"
    if testgen_path.exists():
        testgen = _load_module("agents.test_generator", str(testgen_path))
        if hasattr(testgen, "generate_tests"):
            test_files = testgen.generate_tests(parse_data, output_dir)
        else:
            test_files = _stub_testgen(parse_data, output_dir)
    else:
        print("[WARN] test_generator.py not found -- using stub.")
        test_files = _stub_testgen(parse_data, output_dir)

    print(f"[OK]  Test files: {len(test_files)} file(s) written")
    return test_files


def step_validator(
    python_code: str,
    python_path: str,
    test_cases_path: str,
    output_dir: str,
) -> Dict[str, Any]:
    """
    Agent 5 -- Validator.

    Loads all 226 test cases, dynamically imports the generated Python
    module, runs every test case through it, and compares outputs to
    expected values with \u00a30.01 tolerance.

    Writes validation_report.md with full results.
    Returns PASS only if 100% of tests pass.
    """
    print("\n" + "=" * 72)
    print("STEP 5/7 -- Validator")
    print("=" * 72)

    # --- Load test cases ---
    if not os.path.exists(test_cases_path):
        return {
            "status": "FAIL",
            "issues": [f"Test cases not found at {test_cases_path}"],
            "passed": 0, "total": 0,
        }

    with open(test_cases_path, "r", encoding="utf-8") as fh:
        test_cases = json.load(fh)

    print(f"[VALIDATOR] Loaded {len(test_cases)} test cases from {test_cases_path}")

    # --- Dynamically import generated Python module ---
    spec = importlib.util.spec_from_file_location("generated_module", python_path)
    if spec is None or spec.loader is None:
        return {"status": "FAIL", "issues": [f"Cannot load generated module from {python_path}"], "passed": 0, "total": len(test_cases)}
    gen_mod = importlib.util.module_from_spec(spec)
    sys.modules["generated_module"] = gen_mod
    spec.loader.exec_module(gen_mod)

    if not hasattr(gen_mod, "calculate"):
        return {"status": "FAIL", "issues": ["Generated module has no calculate() function"], "passed": 0, "total": len(test_cases)}

    # --- Run all test cases ---
    from decimal import Decimal, ROUND_HALF_UP
    TOLERANCE = Decimal("0.01")
    CHECKED_FIELDS = [
        "return_code", "monthly_payment", "total_repayable", "total_interest",
        "month_interest", "month_principal", "closing_balance", "penalty",
    ]

    passed = 0
    failed = 0
    failed_cases = []
    category_results = {}

    for case in test_cases:
        inp = case["inputs"]
        expected = case["expected"]
        cat = case.get("category", "unknown")

        try:
            actual = gen_mod.calculate(
                inp["principal"], inp["annual_rate"], inp["term_years"],
                inp["repayment_month"], inp["rate_type"],
            )
        except Exception as exc:
            failed += 1
            failed_cases.append({"id": case["id"], "error": str(exc)})
            category_results.setdefault(cat, {"passed": 0, "failed": 0})["failed"] += 1
            continue

        case_passed = True
        for field in CHECKED_FIELDS:
            exp_val = expected.get(field, 0)
            act_val = getattr(actual, field, 0)

            if field == "return_code":
                match = int(exp_val) == int(act_val)
            else:
                diff = abs(Decimal(str(exp_val)) - Decimal(str(act_val)))
                match = diff <= TOLERANCE

            if not match:
                case_passed = False
                break

        if case_passed:
            passed += 1
        else:
            failed += 1
            failed_cases.append({"id": case["id"], "field": field, "expected": exp_val, "actual": act_val})

        category_results.setdefault(cat, {"passed": 0, "failed": 0})
        if case_passed:
            category_results[cat]["passed"] += 1
        else:
            category_results[cat]["failed"] += 1

    total = passed + failed
    status = "PASS" if failed == 0 else "FAIL"

    # --- Print summary ---
    print(f"[VALIDATOR] Results: {passed}/{total} passed")
    for cat, counts in sorted(category_results.items()):
        print(f"[VALIDATOR]   {cat}: {counts['passed']}/{counts['passed'] + counts['failed']} passed")
    if failed > 0:
        print(f"[VALIDATOR]   {len(failed_cases)} failures (showing first 5):")
        for fc in failed_cases[:5]:
            print(f"[VALIDATOR]     {fc['id']}: {fc.get('field', fc.get('error', 'unknown'))}")

    # --- Write validation_report.md ---
    report = _build_validation_report(passed, failed, total, category_results, failed_cases, test_cases)
    report_path = os.path.join(output_dir, "validation_report.md")
    _ensure_dir(report_path)
    with open(report_path, "w", encoding="utf-8") as fh:
        fh.write(report)
    print(f"[OK]  Validation report written to {report_path}")

    return {
        "status": status,
        "passed": passed,
        "failed": failed,
        "total": total,
        "issues": [f"{fc['id']} failed" for fc in failed_cases],
    }


def step_simplifier(input_py: str, output_py: str) -> None:
    """Agent 6 -- Simplifier."""
    print("\n" + "=" * 72)
    print("STEP 6/7 -- Simplifier")
    print("=" * 72)
    simplifier_path = _REPO_ROOT / "agents" / "simplifier.py"
    if not simplifier_path.exists():
        print("[WARN] simplifier.py not found -- skipping.")
        return
    simplifier = _load_module("agents.simplifier", str(simplifier_path))
    if hasattr(simplifier, "simplify"):
        simplifier.simplify(input_py, output_py)
    else:
        print("[WARN] simplifier.py has no simplify() function -- skipping.")
    print("[OK]  Simplifier step complete")


def step_externaliser(input_py: str, output_dir: str) -> None:
    """Agent 7 -- Externaliser."""
    print("\n" + "=" * 72)
    print("STEP 7/7 -- Externaliser")
    print("=" * 72)
    ext_path = _REPO_ROOT / "agents" / "externaliser.py"
    if not ext_path.exists():
        print("[WARN] externaliser.py not found -- skipping.")
        return
    ext = _load_module("agents.externaliser", str(ext_path))
    if hasattr(ext, "externalise"):
        ext.externalise(input_py, output_dir)
        print(f"[OK]  Business rules written to {output_dir}/business_rules.*")
    else:
        print("[WARN] externaliser.py has no externalise() function -- skipping.")


