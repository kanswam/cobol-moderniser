def _load_module(name: str, path: str) -> Any:
    """Load a Python module from an arbitrary file path."""
    spec = importlib.util.spec_from_file_location(name, path)
    if spec is None or spec.loader is None:
        raise ImportError(f"Cannot load {name} from {path}")
    mod = importlib.util.module_from_spec(spec)
    sys.modules[name] = mod
    spec.loader.exec_module(mod)
    return mod


def _ensure_dir(path: str) -> None:
    """Create parent directories for *path* if they do not exist."""
    Path(path).parent.mkdir(parents=True, exist_ok=True)


def _box_line(text: str, width: int = 72) -> str:
    """Centre *text* inside a box-drawing line."""
    pad = max(0, width - 2 - len(text))
    left = pad // 2
    right = pad - left
    return "\u2551" + " " * left + text + " " * right + "\u2551"


def _build_validation_report(passed, failed, total, category_results, failed_cases, test_cases):
    """Build the validation report markdown."""
    from datetime import datetime
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")

    lines = [
        "# Validation Report \u2014 Migrated Python Module",
        f"*Generated by COBOL Moderniser \u2014 Agent 5 (Validator)*",
        f"*Date: {timestamp}*",
        "",
        "---",
        "",
        f"## Verdict: {'ALL TESTS PASSED' if failed == 0 else f'{failed} TEST(S) FAILED'}",
        "",
        "| Metric | Value |",
        "|---|---|",
        f"| Total test cases | {total} |",
        f"| Passed | {passed} |",
        f"| Failed | {failed} |",
        f"| Pass rate | {passed/total*100:.1f}% |" if total else "| Pass rate | N/A |",
        "| Tolerance | \u00a30.01 (to the penny) |",
        "",
        "---",
        "",
        "## Results by Category",
        "",
        "| Category | Cases | Passed | Failed | Status |",
        "|---|---|---|---|---|",
    ]

    for cat, counts in sorted(category_results.items()):
        cat_total = counts["passed"] + counts["failed"]
        cat_status = "PASS" if counts["failed"] == 0 else "FAIL"
        lines.append(f"| {cat} | {cat_total} | {counts['passed']} | {counts['failed']} | {cat_status} |")

    lines.extend(["", "---", "", "## Sample Test Results", ""])

    # Show first 3 passing cases as examples
    shown = 0
    for case in test_cases:
        if shown >= 3:
            break
        if case["id"] not in [fc["id"] for fc in failed_cases]:
            inp = case["inputs"]
            exp = case["expected"]
            lines.extend([
                f"### {case['id']} \u2014 {case.get('description', 'N/A')}",
                f"**Inputs:** \u00a3{inp['principal']:,.2f} principal, {inp['annual_rate']*100:.3f}% rate, {inp['term_years']}yr term, month {inp['repayment_month']}, {inp['rate_type']} rate",
                "",
                "| Field | Expected |",
                "|---|---|",
                f"| return_code | {exp['return_code']} |",
                f"| monthly_payment | \u00a3{exp['monthly_payment']:,.2f} |",
                f"| total_repayable | \u00a3{exp['total_repayable']:,.2f} |",
                f"| total_interest | \u00a3{exp['total_interest']:,.2f} |",
                f"| closing_balance | \u00a3{exp['closing_balance']:,.2f} |",
                f"| penalty | \u00a3{exp['penalty']:,.2f} |",
                "",
            ])
            shown += 1

    if failed > 0:
        lines.extend(["---", "", "## Failure Details", ""])
        for fc in failed_cases[:10]:
            lines.append(f"- **{fc['id']}**: {fc.get('field', fc.get('error', 'unknown error'))}")
        lines.append("")

    lines.extend([
        "---",
        "",
        "## Migration Certification",
        "",
        f"The migrated Python module has **{'passed all' if failed == 0 else f'passed {passed} of'} {total} test cases**",
        f"with {failed} failure{'s' if failed != 1 else ''} across {len(category_results)} test categories.",
        "",
        "**This report was generated automatically by the COBOL Moderniser pipeline.**",
        "",
        f"*COBOL Moderniser \u2014 {timestamp}*",
        "",
    ])

    return "\n".join(lines)


