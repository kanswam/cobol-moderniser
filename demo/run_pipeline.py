def main() -> None:
    arg_parser = argparse.ArgumentParser(
        description="COBOL Moderniser -- Full 7-Agent Pipeline",
    )
    arg_parser.add_argument(
        "--input", required=True,
        help="Path to the COBOL source file (.cbl)",
    )
    arg_parser.add_argument(
        "--output-dir", default="output",
        help="Directory for all generated artefacts",
    )
    arg_parser.add_argument(
        "--no-ai", action="store_true",
        help="Skip the AI interpretation step (Agent 2) -- free mode",
    )
    arg_parser.add_argument(
        "--no-cost", action="store_true",
        help="Disable cost tracking even when AI is used",
    )
    arg_parser.add_argument(
        "--skip-simplify", action="store_true",
        help="Skip Agent 6 (Simplifier)",
    )
    args = arg_parser.parse_args()

    cobol_path = args.input
    out_dir = Path(args.output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    # ------------------------------------------------------------------
    # Cost tracker -- created once at pipeline start.
    # ------------------------------------------------------------------
    tracker: Optional[Any] = None
    if CostTracker is not None and not args.no_cost:
        tracker = CostTracker()
        print("[INFO] Cost tracking enabled.")
    else:
        print("[INFO] Cost tracking disabled.")

    overall_start = time.perf_counter()

    # ------------------------------------------------------------------
    # Step 1 -- Parser
    # ------------------------------------------------------------------
    parser_json = str(out_dir / "parser_output.json")
    parse_data = step_parser(cobol_path, parser_json)

    # ------------------------------------------------------------------
    # Step 2 -- Logic Extractor
    # ------------------------------------------------------------------
    logic_md = str(out_dir / "business_rules.md")
    logic_json = str(out_dir / "logic_output.json")
    extracted = step_logic(
        parse_data,
        logic_md,
        logic_json,
        no_ai=args.no_ai,
        tracker=tracker,
    )

    # ------------------------------------------------------------------
    # Step 3 -- Test Generator (BEFORE code is written -- test-driven)
    # Writes test cases to repo root so Step 5 always finds them.
    # ------------------------------------------------------------------
    test_dir = str(_REPO_ROOT / "tests" / "generated")
    test_files = step_testgen(parse_data, test_dir)

    # ------------------------------------------------------------------
    # Step 4 -- Code Generator (AFTER tests exist -- writes code to pass tests)
    # ------------------------------------------------------------------
    python_out = str(out_dir / "generated.py")
    python_code = step_codegen(parse_data, extracted, python_out)

    # ------------------------------------------------------------------
    # Step 5 -- Validator
    # Reads test cases from repo root (same location Step 3 writes to).
    # ------------------------------------------------------------------
    test_cases_path = str(_REPO_ROOT / "tests" / "generated" / "all_test_cases.json")
    validation = step_validator(python_code, python_out, test_cases_path, str(out_dir))

    # ------------------------------------------------------------------
    # Step 6 -- Simplifier (optional)
    # ------------------------------------------------------------------
    if not args.skip_simplify:
        simplified_py = str(out_dir / "simplified.py")
        step_simplifier(python_out, simplified_py)

    # ------------------------------------------------------------------
    # Step 7 -- Externaliser
    # ------------------------------------------------------------------
    step_externaliser(python_out, str(out_dir))

    overall_elapsed = time.perf_counter() - overall_start

    # ------------------------------------------------------------------
    # Cost report (before final summary so it appears above the box).
    # ------------------------------------------------------------------
    if tracker is not None and tracker.has_records:
        tracker.print_summary()
        cost_report_path = str(out_dir / "cost_report.json")
        tracker.save(cost_report_path)
        print(f"[OK]  Cost report saved to {cost_report_path}")

    # ------------------------------------------------------------------
    # Final summary box
    # ------------------------------------------------------------------
    print("\n\u2554" + "\u2550" * 74 + "\u2557")
    print("\u2551               COBOL MODERNISER \u2014 PIPELINE COMPLETE                           \u2551")
    print("\u2560" + "\u2550" * 74 + "\u2563")

    status_text = "PASS" if validation["status"] == "PASS" else "FAIL"
    status_colour_tag = ""  # terminals that support it can add ANSI codes
    print(f"\u2551   Status        : {status_text:<58} \u2551")
    print(f"\u2551   Input         : {cobol_path:<58} \u2551")
    print(f"\u2551   Output dir    : {str(out_dir):<58} \u2551")
    print(f"\u2551   Duration      : {overall_elapsed:.1f}s{'':<54} \u2551")
    print(f"\u2551   Pipeline      : 7-Agent Pipeline{'':<35} \u2551")
    print("\u2551\u2500" + "\u2500" * 73 + "\u2562")

    # Cost line (only when applicable)
    if tracker is not None and tracker.has_records:
        cost = tracker.total_cost_usd
        tokens = tracker.total_tokens
        cost_line = f"API cost:  ${cost:.4f}  |  {tokens:,} tokens"
        print(f"\u2551   {cost_line:<69} \u2551")
    elif args.no_ai:
        print(f"\u2551   API cost:  $0.0000  |  0 tokens ( --no-ai mode ){'':<17} \u2551")
    elif CostTracker is None:
        print(f"\u2551   API cost:  n/a  (cost_tracker module unavailable){'':<19} \u2551")

    print("\u2551\u2500" + "\u2500" * 73 + "\u2562")
    print(f"\u2551   Business rules: {logic_md:<58} \u2551")
    passed = validation.get("passed", 0)
    total = validation.get("total", 0)
    print(f"\u2551   Validation    : {passed}/{total} tests passed{'':<33} \u2551")
    print("\u255a" + "\u2550" * 74 + "\u255d\n")


if __name__ == "__main__":
    main()

